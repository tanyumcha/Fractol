#include "libft.h"

char	*ft_strcpy(char *dst, const char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		dst[i] = str[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
