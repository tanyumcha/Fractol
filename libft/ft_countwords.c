#include "libft.h"

int	ft_countwords(char c, char *d)
{
	int	i;
	int	w;

	if (d == NULL)
		return (-1);
	w = 0;
	i = 0;
	while (d[i] != '\0')
	{
		if (d[i] != c && d[i] != '\0')
		{
			w++;
			while (d[i] != c && d[i] != '\0')
				i++;
		}
		if (d[i] == '\0')
			return (w);
		i++;
	}
	return (w);
}
