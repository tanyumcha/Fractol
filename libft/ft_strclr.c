#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;

	if (s == NULL)
		return ;
	if (*s == '\0')
		return ;
	i = ft_strlen(s);
	while (i > 0)
	{
		*s = '\0';
		s++;
		i--;
	}
	return ;
}
